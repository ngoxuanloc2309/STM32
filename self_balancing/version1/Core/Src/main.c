#define u8 uint8_t
#define u16 uint16_t
MPU6050_t MPU6050;
float roll =0.0, filtered_roll = 0.0, previous_roll=0.0;
float dt=0.01;
float Error, pre_Error, pre_pre_Error, Out, pre_Out;
float Kp=10, Ki=0.5, Kd=1;
u8 max_PID = 255;

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim->Instance == htim1.Instance){
		MPU6050_Read_All(&hi2c1, &MPU6050);
		roll = MPU6050.KalmanAngleX;
		filtered_roll = 0.5f * previous_roll + 0.5f * roll;
		previous_roll = filtered_roll;
	}
}

void control_motor(u8 motor, u8 dir){
	if(motor==1){
		if(dir==1){
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, 1);
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, 0);
		}
		else{
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, 0);
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, 1);		
		}
	}
	else
	if(motor==2){
		if(dir==1){
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_10, 1);
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_11, 0);
		}
		else{
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_10, 0);
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_11, 1);		
		}	
	}
}

void PID_Control(u8 setpoint, float angle_current){
	Error = setpoint - angle_current;
	float P_part = Kp*(Error - pre_Error);
	float I_part = 0.5*Ki*dt*(Error + pre_Error);
	float D_part= Kd/dt*( Error - 2*pre_Error+ pre_pre_Error);
	Out = pre_Out + P_part + I_part + D_part ;
	// to do sth
	pre_pre_Error = pre_Error;
	pre_Error = Error;
	pre_Out = Out;
	if(Out>max_PID){
		Out = max_PID;
	}else
	if(Out < 5){
		Out = 0;
	}
}

