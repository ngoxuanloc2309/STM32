#define COUNTER_MAX 65536          
#define COUNTER_MID (COUNTER_MAX / 2)          
#define RESET_VALUE 0            
#define dt 0.01f  // 10ms

volatile short count_chuan = 0;
volatile long long xung = 0;

float Error, vel_current, P_part, I_part, D_part;
float pre_Error = 0, pre_pre_Error = 0;
float vel = 0, delta;
uint8_t Out = 0;
float pre_Out = 0;
int32_t prev_xung=0;
float Kp = 5.0f, Ki = 320.f, Kd = 3.0002;

void control_motor(uint8_t dir) {
    if (dir > 0) {
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_10, 1);
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_11, 0);
    } else {
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_10, 0);
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_11, 1);
    }
}

int32_t processEncoderCount(void) {
    uint16_t raw_count = __HAL_TIM_GET_COUNTER(&htim2); 
    int32_t calibrated_count;                       

    if (raw_count >= COUNTER_MID) {
        calibrated_count = -(COUNTER_MAX - raw_count);
    } else {
        calibrated_count = raw_count;
    }

    if (calibrated_count >= COUNTER_MID || calibrated_count <= -COUNTER_MID) {
        calibrated_count = 0;
        __HAL_TIM_SET_COUNTER(&htim2, 0);
    }

    return calibrated_count;
}

float calculated_vel(int32_t current_xung) {
    delta = current_xung - prev_xung;

    if (delta > COUNTER_MID) delta -= COUNTER_MAX;
    else if (delta < -COUNTER_MID) delta += COUNTER_MAX;

    float velocity = (delta * 60.0f) / (985.0f * dt);  // RPM
    prev_xung = current_xung;

    return velocity;
}

void PID(int16_t setpoint) {
    count_chuan = processEncoderCount();

    vel_current = calculated_vel(count_chuan);
    Error = (float)setpoint - vel_current;

    P_part = Kp * (Error - pre_Error);
    I_part = 0.5f * Ki * dt * (Error + pre_Error);
    D_part = Kd / dt * (Error - 2.0f * pre_Error + pre_pre_Error);

    Out = pre_Out + P_part + I_part + D_part;

    if (Out > 255) Out = 255;
    if (Out < 0) Out = 0;

    if (setpoint >= 0) {
        control_motor(0);
    } else {
        control_motor(1);
    }

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint8_t)Out);

    pre_pre_Error = pre_Error;
    pre_Error = Error;
    pre_Out = Out;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM1) {
        PID(+80); 
    }
}

