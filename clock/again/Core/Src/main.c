uint8_t decimal_bcd(uint8_t value){
	return ((value/10)<<4)|(value%10);
}

uint8_t bcd_decimal(uint8_t bcd){
	// 0b0100 0111 -> 47
	// value>>4 ->0000 0100
	return ((bcd >> 4) * 10) + (bcd & 0x0F);
}

void set_time(uint8_t second, uint8_t minute, uint8_t hour, uint8_t day_of_week, uint8_t day_of_month, uint8_t month, uint16_t year){
	set_time_ne[0] = decimal_bcd(second);
	set_time_ne[1] = decimal_bcd(minute);
	set_time_ne[2] = decimal_bcd(hour);
	set_time_ne[3] = decimal_bcd(day_of_week);
	set_time_ne[4] = decimal_bcd(day_of_month);
	set_time_ne[5] = decimal_bcd(month);
	set_time_ne[6] = decimal_bcd(year);
	HAL_I2C_Mem_Write(&hi2c1, DS3231_DEVICE_ID, 0x00, 1, set_time_ne, 7, 100);
}

void get_time(){
	HAL_I2C_Mem_Read(&hi2c1, DS3231_DEVICE_ID, 0x00, 1, get_time_ne, 7, 100);
	clock.seconds = bcd_decimal(get_time_ne[0]);
	clock.minutes = bcd_decimal(get_time_ne[1]);
	clock.hours = bcd_decimal(get_time_ne[2]);
	clock.day_of_weeks = bcd_decimal(get_time_ne[3]);
	clock.day_of_months = bcd_decimal(get_time_ne[4]);
	clock.months = bcd_decimal(get_time_ne[5]);
	clock.years = bcd_decimal(get_time_ne[6]);
}

